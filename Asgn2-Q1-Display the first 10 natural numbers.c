//Question: Write a program in C to display the first 10 natural numbers.
#include <stdio.h>

int main()
{
    //var
    int i;

    //output
    for(i=1; i<=10; i++)
    {
        printf("%d ", i);
    }
    printf("\n");
    return 0;
}
